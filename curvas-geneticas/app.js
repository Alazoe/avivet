// ── SELECTORES DE LÍNEA ────────────────────────────────────────────────
function renderSelectores() {
  const cont = document.getElementById('sel-lineas');
  cont.innerHTML = Object.keys(LINEAS).map(k => `
    <button class="btn-linea${k === lineaSel ? ' activo' : ''}"
            style="--lcolor:${LINEAS[k].color}"
            onclick="selLinea('${k}')">${k}</button>
  `).join('');
}

function selLinea(nombre) {
  lineaSel = nombre;
  renderSelectores();
  actualizarTodo();
}

function cambiarAves() {
  const v = parseInt(document.getElementById('inp-aves').value);
  if (v > 0) { nAves = v; renderEquipamiento(); }
}

function selTab(tab) {
  tabActual = tab;
  ['crianza','postura','equipamiento'].forEach(t => {
    document.getElementById('tab-' + t).classList.toggle('activo', t === tab);
    document.getElementById('panel-' + t).style.display = t === tab ? '' : 'none';
  });
}

