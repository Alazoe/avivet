// ── GRÁFICO SVG ────────────────────────────────────────────────────────
function renderLinea(svgId, datos, iMin, iMax, titulo, yMin, yMax, color, singleLine) {
  const wrap = document.getElementById(svgId);
  if (!wrap) return;

  const W = 800, H = 220, PL = 52, PR = 20, PT = 28, PB = 40;
  const CW = W - PL - PR, CH = H - PT - PB;

  const vals = datos.flatMap(r => [r[iMin], iMin !== iMax ? r[iMax] : r[iMin]]);
  const lo   = yMin !== null && yMin !== undefined ? yMin : Math.min(...vals) * 0.95;
  const hi   = yMax !== null && yMax !== undefined ? yMax : Math.max(...vals) * 1.05;

  const xs = datos.map(r => PL + (r[0] - datos[0][0]) / (datos[datos.length-1][0] - datos[0][0]) * CW);
  const yS = v => PT + CH - (v - lo) / (hi - lo) * CH;

  // Grid lines
  const gridN = 5;
  let grids = '';
  for (let i = 0; i <= gridN; i++) {
    const v  = lo + (hi - lo) * i / gridN;
    const yy = yS(v);
    grids += `<line x1="${PL}" y1="${yy}" x2="${W-PR}" y2="${yy}" stroke="#e8e4dc" stroke-width="1"/>`;
    grids += `<text x="${PL-6}" y="${yy+4}" text-anchor="end" font-size="9" fill="#aaa">${v < 10 ? v.toFixed(2) : Math.round(v)}</text>`;
  }

  // X labels cada 10 semanas
  let xlabels = '';
  datos.forEach(r => {
    if ((r[0] - datos[0][0]) % 10 === 0 || r[0] === datos[0][0]) {
      const xx = PL + (r[0] - datos[0][0]) / (datos[datos.length-1][0] - datos[0][0]) * CW;
      xlabels += `<text x="${xx}" y="${H-PB+14}" text-anchor="middle" font-size="9" fill="#aaa">${r[0]}</text>`;
    }
  });

  // Band min→max
  let band = '';
  if (iMin !== iMax) {
    const pts1 = datos.map((r,i) => `${xs[i]},${yS(r[iMin])}`).join(' ');
    const pts2 = [...datos].reverse().map((r,i) => `${xs[datos.length-1-i]},${yS(r[iMax])}`).join(' ');
    band = `<polygon points="${pts1} ${pts2}" fill="${color}" fill-opacity="0.15"/>`;
  }

  // Line min
  const lineMin = `<polyline points="${datos.map((r,i) => `${xs[i]},${yS(r[iMin])}`).join(' ')}"
    fill="none" stroke="${color}" stroke-width="2.5" stroke-linejoin="round"/>`;
  // Line max (if different)
  const lineMax = iMin !== iMax
    ? `<polyline points="${datos.map((r,i) => `${xs[i]},${yS(r[iMax])}`).join(' ')}"
        fill="none" stroke="${color}" stroke-width="1.5" stroke-dasharray="4 3" stroke-linejoin="round" opacity="0.7"/>`
    : '';

  wrap.innerHTML = `<svg viewBox="0 0 ${W} ${H}" xmlns="http://www.w3.org/2000/svg">
    <text x="${PL}" y="16" font-size="11" fill="#555" font-weight="600">${titulo}</text>
    ${grids}
    <text x="${PL + CW/2}" y="${H-PB+28}" text-anchor="middle" font-size="9" fill="#bbb">Semana</text>
    ${xlabels}
    ${band}${lineMin}${lineMax}
  </svg>`;
}

