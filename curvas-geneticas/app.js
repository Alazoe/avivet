// ── INICIAR — CALCULAR EDAD ────────────────────────────────────────────
function calcularEdad() {
  const val = document.getElementById('inp-nacimiento').value;
  const res = document.getElementById('edad-resultado');
  if (!val) { res.style.display = 'none'; return; }

  const nac = new Date(val + 'T00:00:00');
  const hoy = new Date(); hoy.setHours(0, 0, 0, 0);
  const diasDesdeNac = Math.floor((hoy - nac) / 86400000);

  if (diasDesdeNac < 0) {
    res.style.display = 'none';
    return;
  }

  const diaVida = diasDesdeNac + 1;          // día 1 = día de nacimiento
  const semana  = Math.ceil(diaVida / 7);    // sem 1 = días 1-7, sem 2 = días 8-14, ...

  const L = LINEAS[lineaSel];
  const enCrianza = semana <= L.crianzaSem;
  const etapaLabel = enCrianza ? 'Crianza' : 'Postura';
  const etapaColor = enCrianza ? '#1b4332' : '#b71c1c';
  const etapaFondo = enCrianza ? '#e8f5e9'  : '#ffebee';

  // Días restantes hasta el inicio de la siguiente semana
  const diasEnSemActual = ((diaVida - 1) % 7) + 1;
  const diasParaSigSem  = 7 - diasEnSemActual;
  const proxFecha = new Date(hoy);
  proxFecha.setDate(hoy.getDate() + diasParaSigSem + 1);
  const proxStr = proxFecha.toLocaleDateString('es-CL', { day: 'numeric', month: 'long' });

  document.getElementById('edad-semana').textContent = 'Semana ' + semana;
  document.getElementById('edad-dias').textContent   = 'Día ' + diaVida + ' de vida · ' + diasDesdeNac + ' días desde el nacimiento';
  document.getElementById('edad-etapa').textContent  = etapaLabel;
  document.getElementById('edad-etapa').style.background = etapaFondo;
  document.getElementById('edad-etapa').style.color      = etapaColor;
  document.getElementById('edad-prox').textContent   =
    diasParaSigSem === 0
      ? 'Hoy comienza la semana ' + (semana + 1)
      : 'Semana ' + (semana + 1) + ' comienza el ' + proxStr + ' (' + diasParaSigSem + ' días)';

  // Días de vida al nacer (día 1) del lote
  const edadMeses = (diasDesdeNac / 30.44).toFixed(1);
  document.getElementById('edad-dias-nacer').textContent = edadMeses + ' meses de edad';

  res.style.display = 'flex';
}

// ── EXPORTAR A EXCEL ───────────────────────────────────────────────────
function exportarExcel() {
  const L  = LINEAS[lineaSel];
  const wb = XLSX.utils.book_new();

  const mC = L.mortCrianza || null;
  const mP = L.mortPostura || null;

  // ── Hoja 1: Crianza ──────────────────────────────────────────────────
  const mortNotaC = mC && lineaSel !== 'Hy-Line W-80' ? ' (*) Mortalidad estimada basada en perfil W-80.' : '';
  const crianzaData = [
    ['Línea genética:', lineaSel],
    ['Fuente:', L.fuente + mortNotaC],
    [],
    ['Semana', 'Peso mín (kg)', 'Peso máx (kg)',
     'Alimento mín (g/ave/día)', 'Alimento máx (g/ave/día)',
     'Agua mín (ml/ave/día)', 'Agua máx (ml/ave/día)',
     ...(mC ? ['Mort. semanal (%)', 'Mort. acumulada (%)'] : [])],
  ];
  L.crianza.forEach((r, i) => {
    const mA = mC ? mC[i] : null;
    const mS = mC ? mortSem(mC, i) : null;
    crianzaData.push([r[0], r[1], r[2], r[3], r[4], r[5], r[6], ...(mC ? [mS, mA] : [])]);
  });
  const wsCrianza = XLSX.utils.aoa_to_sheet(crianzaData);
  wsCrianza['!cols'] = [8,14,14,22,22,22,22,...(mC ? [18,18] : [])].map(w => ({ wch: w }));
  XLSX.utils.book_append_sheet(wb, wsCrianza, 'Crianza');

  // ── Hoja 2: Postura ──────────────────────────────────────────────────
  const mortNotaP = mP && lineaSel !== 'Hy-Line W-80' ? ' (*) Mortalidad estimada basada en perfil W-80.' : '';
  const posturaData = [
    ['Línea genética:', lineaSel],
    ['Fuente:', L.fuente + mortNotaP],
    [],
    ['Semana', 'Peso mín (kg)', 'Peso máx (kg)', '% Postura', 'Peso huevo (g)',
     'Alimento mín (g/ave/día)', 'Alimento máx (g/ave/día)',
     'Agua mín (ml/ave/día)', 'Agua máx (ml/ave/día)',
     ...(mP ? ['Mort. semanal (%)', 'Mort. acumulada (%)'] : [])],
  ];
  L.postura.forEach((r, i) => {
    const mA = mP ? mP[i] : null;
    const mS = mP ? mortSem(mP, i) : null;
    posturaData.push([r[0], r[1], r[2], r[3], r[4], r[5], r[6], r[7], r[8], ...(mP ? [mS, mA] : [])]);
  });
  const wsPostura = XLSX.utils.aoa_to_sheet(posturaData);
  wsPostura['!cols'] = [8,14,14,12,15,22,22,22,22,...(mP ? [18,18] : [])].map(w => ({ wch: w }));
  XLSX.utils.book_append_sheet(wb, wsPostura, 'Postura');

  // ── Hoja 3: Equipamiento ─────────────────────────────────────────────
  const n           = nAves;
  const mCrianza    = Math.ceil(n * 0.14);
  const perchasC    = Math.ceil(n * 15 / 100);
  const bebC        = Math.ceil(n / 125);
  const nipC        = Math.ceil(n / 12.5);
  const comC        = Math.ceil(n * 2 / 100);
  const nidosInd    = Math.ceil(n / 5);
  const nidosComM   = Math.ceil(n / 120);
  const bebP        = Math.ceil(n / 100);
  const nipP        = Math.ceil(n / 12);
  const comP        = Math.ceil(n * 4 / 100);
  const perchasP    = Math.ceil(n * 15 / 100);
  const perchasEl   = Math.ceil(perchasP * 0.20);
  const extM2       = Math.ceil(n * 0.19);

  const eqData = [
    ['N° de aves:', n],
    [],
    ['CRIANZA'],
    ['Ítem', 'Valor', 'Unidad', 'Ratio'],
    ['Superficie galpón',  mCrianza,   'm²',          `${(n / mCrianza).toFixed(1)} aves/m²`],
    ['Perchas',            perchasC,   'm lineales',   '15 cm/ave'],
    ['Bebederos campana',  bebC,       'unidades',     '1 c/125 aves'],
    ['Nipples',            nipC,       'unidades',     '1 c/12 aves'],
    ['Comederos lineales', comC,       'm',            '2 cm/ave'],
    [],
    ['POSTURA'],
    ['Ítem', 'Valor', 'Unidad', 'Ratio'],
    ['Nidos individuales', nidosInd,   'unidades',     '1 c/5 aves'],
    ['Nidos comunitarios', nidosComM,  'm lineales',   '1 m c/120 aves'],
    ['Bebederos campana',  bebP,       'unidades',     '1 c/100 aves'],
    ['Nipples',            nipP,       'unidades',     '1 c/12 aves'],
    ['Comederos lineales', comP,       'm',            '4 cm/ave'],
    ['Perchas totales',    perchasP,   'm lineales',   '15 cm/ave'],
    ['Perchas elevadas',   perchasEl,  'm lineales',   '≥ 20% del total'],
    ['Acceso exterior',    extM2,      'm²',           '0,19 m²/ave'],
  ];
  const wsEq = XLSX.utils.aoa_to_sheet(eqData);
  wsEq['!cols'] = [22, 10, 14, 18].map(w => ({ wch: w }));
  XLSX.utils.book_append_sheet(wb, wsEq, 'Equipamiento');

  // ── Hoja 4: Tamaño de Huevo ──────────────────────────────────────────
  const tamResult = getTamHuevoData();
  if (tamResult) {
    const { datos: tamDatos, esRef } = tamResult;
    const fuenteTam = esRef
      ? '(*) Referencia aplicada desde Hy-Line Brown Jaula 2024 — pendiente datos específicos de esta línea'
      : 'Hy-Line Brown Jaula 2024 · Tabla de calidad y distribución del tamaño de huevo';
    const tamData = [
      ['Línea genética:', lineaSel],
      ['Fuente:', fuenteTam],
      ['Nota:', 'Datos biweekly (sem pares). Clasificación por peso promedio del huevo semanal.'],
      [],
      ['Semana', '% Chico 43-53 g', '% Mediano 53-63 g', '% Grande 63-73 g', '% Muy grande >73 g'],
    ];
    tamDatos.forEach(r => tamData.push([r[0], r[1], r[2], r[3], r[4]]));
    const wsTam = XLSX.utils.aoa_to_sheet(tamData);
    wsTam['!cols'] = [10, 18, 20, 18, 20].map(w => ({ wch: w }));
    XLSX.utils.book_append_sheet(wb, wsTam, 'Tamaño Huevo');
  }

  const fecha    = new Date().toISOString().slice(0, 10);
  const nombreFn = `curva-genetica_${lineaSel.replace(/[\s/]/g, '-')}_${fecha}.xlsx`;
  XLSX.writeFile(wb, nombreFn);
}
