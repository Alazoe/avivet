// ── HELPER MORTALIDAD ─────────────────────────────────────────────────
function mortSem(arr, i) {
  if (!arr || arr[i] == null) return null;
  return i === 0 ? arr[0] : Math.max(0, parseFloat((arr[i] - arr[i - 1]).toFixed(2)));
}

